#include "libft.h"

uint32_t	ft_uint32_reverse(uint32_t	num)
{
	return (((num & 0xff000000) >> 24) |
			((num & 0x00ff0000) >> 8) |
			((num & 0x0000ff00) << 8) |
			(num << 24));
}
